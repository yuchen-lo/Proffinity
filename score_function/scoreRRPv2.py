def scoreRRP(pdbid, ctype):


    if (ctype == 'CA'):
        file1 = open('./raw_graphv2/'+pdbid+'_CA_output.txt', 'r')
    elif (ctype == 'CB'):
        file1 = open('./raw_graphv2/'+pdbid+'_CB_output.txt', 'r')
   
    Lines = file1.readlines()
    total_lines=len(Lines)

    if (ctype == 'CA'):
        with open('./raw_graphv2/'+pdbid+'_CA_output.txt', 'w') as f:
            print("")
    elif (ctype == 'CB'):
        with open('./raw_graphv2/'+pdbid+'_CB_output.txt', 'w') as f:
            print("")


    resPid = Lines[0].split(',')[2]
    Ec=0
    Etot=0
    m=6
    n=4
    num_lines=0
    last_line=0

    # Strips the newline character
    for line in Lines:
        resA=line.split(',')[1]
        resAid=line.split(',')[2]
        resB=line.split(',')[4]
        dist=float(line.split(',')[6])
        num_lines=num_lines+1
        
        e1k=e1[(resA,resB)]
        e2k=e2[(resA,resB)]
        e3k=e3[(resA,resB)]
        r1k=r1[(resA,resB)]
        r2k=r2[(resA,resB)]
        r3k=r3[(resA,resB)]
        d2k=d2[(resA,resB)]
        d3k=d3[(resA,resB)]
        
        #E1=r1k**m/dist**m
        E1=e1k/(n-m)*(n*r1k**m/dist**m - m*r1k**n/dist**n)
        E2=e2k*exp(-0.5*((dist-r2k)/d2k)**2)
        E3=e3k*exp(-0.5*((dist-r3k)/d3k)**2)
        Eij=-1*(E1+E2+E3)
        
        line=line.strip("\n")+","+str(round(Eij,2))
        if (ctype == 'CA'):
            with open('./raw_graphv2/'+pdbid+'_CA_output.txt', 'a') as f:
                print(line, file=f)
        if (ctype == 'CB'):
            with open('./raw_graphv2/'+pdbid+'_CB_output.txt', 'a') as f:
                print(line, file=f)
                
                
        if (num_lines==total_lines):
            last_line=1
            Ec=Ec+Eij
        else:
            last_line=0
    
        if (resAid != resPid or last_line==1):    
            Etot=Etot+Ec
            #print("resPid:"+resPid+" Etot:"+str(round(Etot,2))+" Ec:"+str(round(Ec,2))+" lines:"+str(num_lines)+"/"+str(total_lines))
            Ec=0
            resPid=resAid
    

        Ec=Ec+Eij

    print("scoreRRP:",str(round(Etot,2))," ctype:",ctype)
    return round(Etot,2)



